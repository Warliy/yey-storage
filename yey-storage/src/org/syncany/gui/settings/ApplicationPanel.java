    private void btnImageBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImageBrowseActionPerformed
    File imageFile = FileUtil.showBrowseFileDialog();
	
    if (imageFile == null)
        return;
	
    ImageIcon imageIcon = new ImageIcon(imageFile.getAbsolutePath());
	
    if (imageIcon.getImageLoadStatus() != MediaTracker.COMPLETE) {
        JOptionPane.showMessageDialog(this, resourceBundle.getString("app_err_upload"));	    
        return;
    }
	
    cachedUserImage = ImageUtil.getScaledImage(imageIcon.getImage(), Constants.PROFILE_IMAGE_MAX_WIDTH, Constants.PROFILE_IMAGE_MAX_HEIGHT);
	
    imageIcon.setImage(cachedUserImage);
    lblUserImage.setIcon(imageIcon);
    }//GEN-LAST:event_btnImageBrowseActionPerformed

    private void rdImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rdImageActionPerformed
    btnImageBrowse.setEnabled(rdImageOther.isSelected());
	
    if (rdImageNone.isSelected()) {
        ImageIcon imageIcon = new ImageIcon(config.getUserImage(Config.UserImageType.None));
        lblUserImage.setIcon(imageIcon);
    }
	
    else if (rdImageSystem.isSelected()) {
        ImageIcon imageIcon = new ImageIcon(config.getUserImage(Config.UserImageType.System));
        lblUserImage.setIcon(imageIcon);
    }
	
    else if (rdImageOther.isSelected()) {
        lblUserImage.setIcon(new ImageIcon(cachedUserImage));	
    }			
    }//GEN-LAST:event_rdImageActionPerformed


