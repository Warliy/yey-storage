    @Override
    public void load() {
        chkStartup.setSelected(config.isAutostart());
        chkShowNotifications.setSelected(config.isNotificationsEnabled());
        txtName.setText(config.getUserName());
        txtMachineName.setText(config.getMachineName());
	
    // User Image
    btnImageBrowse.setEnabled(rdImageOther.isSelected());
	
    switch (config.getUserImageType()) {
        case None:
        rdImageNone.setSelected(true);		
        break;
		
        case Other:
        rdImageOther.setSelected(true);
        break;
		
        case System:
        default:
        rdImageSystem.setSelected(true);		
    }
	
    ImageIcon imageIcon = new ImageIcon(config.getUserImage());
	
    cachedUserImage = config.getUserImage(Config.UserImageType.Other);
    lblUserImage.setIcon(imageIcon);
    }

    @Override
    public void save() {
        config.setAutostart(chkStartup.isSelected());
        config.setNotificationsEnabled(chkShowNotifications.isSelected());
        config.setUserName(txtName.getText());
        config.setMachineName(txtMachineName.getText());
	
    // User Image
    if (rdImageNone.isSelected()) {
        config.setUserImageType(Config.UserImageType.None);
    }
	
    else if (rdImageSystem.isSelected()) {
        config.setUserImageType(Config.UserImageType.System);
    }
	
    else if (rdImageOther.isSelected()) {
        config.setUserImageType(Config.UserImageType.Other);
	    
        // Save image to 'profile.png'
        BufferedImage writeImage = ImageUtil.toBufferedImage(cachedUserImage);
        File profileImageFile = new File(config.getConfDir()+File.separator+"profile.png");

        try {
        ImageIO.write(writeImage, "png", profileImageFile);
        }
        catch (IOException e) {
        logger.warning("Unable to save image to "+profileImageFile);
        }
    }
    }

